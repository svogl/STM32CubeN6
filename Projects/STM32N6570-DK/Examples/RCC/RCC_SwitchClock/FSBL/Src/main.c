/**
  * @brief  switch in system clock out of ISR context.
  * @retval None
  */
static void SwitchSystemClock(void)
{
  switch (ClockSourceStatus)
  {
  case CLOCK_SOURCE_PLL1_HSI:
    /* Cpu and System clock sources are PLL1 HSI oscillator */
    /* Switch to PLL1_HSE */
    SystemClockPLL1HSE_Config();
    ClockSourceStatus = CLOCK_SOURCE_PLL1_HSE;
    break;
  case CLOCK_SOURCE_PLL1_HSE:
    /* Cpu and System clock sources are PLL1 HSE oscillator */
    /* Switch to PLL2 HSI */
    SystemClockPLL2HSI_Config();
    ClockSourceStatus = CLOCK_SOURCE_PLL2_HSI;
    break;
  case CLOCK_SOURCE_PLL2_HSI:
    /* Cpu and System clock sources are PLL2 HSI oscillator */
    /* Switch to PLL2 HSE*/
    SystemClockPLL2HSE_Config();
    ClockSourceStatus = CLOCK_SOURCE_PLL2_HSE;
    break;
  case CLOCK_SOURCE_PLL2_HSE:
    /* Cpu and System clock sources are PLL2 HSE oscillator */
    /* Switch to HSI */
    SystemClockHSI_Config();
    ClockSourceStatus = CLOCK_SOURCE_HSI;
    break;
  case CLOCK_SOURCE_HSI:
    /* Cpu and System clock sources are HSI oscillator */
    /* Switch to HSE */
    SystemClockHSE_Config();
    ClockSourceStatus = CLOCK_SOURCE_HSE;
    break;
  case CLOCK_SOURCE_HSE:
    /* Cpu and System clock sources are HSE oscillator */
    /* Switch to PLL1 HSI */
    SystemClockPLL1HSI_Config();
    ClockSourceStatus = CLOCK_SOURCE_PLL1_HSI;
    break;
  default:
    break;
  }
}

