      HAL_TIMEx_ConfigAsymmetricalDeadTime(&htim1, aDT[DEADTIME_ARRAY_SIZE-i-1]); /* Set the falling deadtime */

      i++;
      HAL_Delay(250); /* Add 250ms delay to prevent button bounce */
      buttonPressedFlag = 0;
