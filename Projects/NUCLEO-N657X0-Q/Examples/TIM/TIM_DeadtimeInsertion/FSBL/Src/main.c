/* -----------------------------------------------------------------------
Deadtime values computation:

    In this example, the desired deadtime durations are 12us, 16us and 20us.
    The values are computed for the rising deadtime (BDTR.DTG) and falling deadtime (DTR2.DTGF),
    as they work the same way.
    The register configuration can be found empirically.

    The parameters we can play with are the clock division (CKD) and the value in DTG(F)[7:5].
    If is not enough to find the right configuration, the TIM_CLK has to be changed.

    Depending the value put in DTG(F)[7:5] bitfields, the deadtime formula calculation is different
    (cf. BDTR.DTG bitfield description).

    On top of that, the reference time, i.e. deadtime and sampling time (tDTS) can be modified by a
    clock division (CKD bitfield in CR1 register). So tDTS can be once, twice or four times the tTIM_KER_CK.

    It works as follows:
     1) tTIM_KER_CK
     2) tDTS (Deadtime and Sampling time)
     3) tDTG (Deadtime generator time)
     4) DT (Deadtime time)

    So for our combination:
     tDTS = 4 x tTIM_KER_CK = 20ns  (with tTIM_KER_CK = 5ns and CR1.CKD = DIV4)

     tDTG = 16 x tDTS = 320ns  (with DTG(F)[7:5] = 111)

     Because DTG(F)[7:5] = 111, DT = (32 + DTG(F)[4:0])*tDTG with DT the deadtime duration in s.

     Deadtime values range:
      10240ns to 20160ns by 320ns steps

    When we have the desired range, the value to put in the register is:
     DTG(F) = DTG(F)[7:5] | DTG(F)[4:0] with:

     DTG(F)[4:0] = (DT / tDTG) - 32

    e.g. 12us, DTG(F)[4:0] = (12e-6 / 320e-9) - 32
                             = 5.5
                             ~ 0x5
  ----------------------------------------------------------------------- */
const uint32_t aDT[DEADTIME_ARRAY_SIZE] = {0xE5, 0xF2, 0xFE}; /* Deadtime values to put in the register */

